#include "simplecalc.h"

double simplecalc::subtract(double a, double b)
{
    return a - b;
}

double simplecalc::add(double a, double b)
{
    return a + b;
}

double simplecalc::divide(double a, double b)
{
    return a / b;
}

double simplecalc::multiply(double a, double b)
{
    return a * b;
}
